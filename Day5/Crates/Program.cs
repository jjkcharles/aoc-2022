        private static void MoveCrates(Stack<string>[] input, 
                                        List<MoveInstruction> inst,
                                        bool MoveMultiple=false)
        {
            foreach(var a in inst)
            {
                //PrintStacks(input);
                Console.WriteLine("Performing {0}", a);
                var tempStack = new Stack<string>();
                for(int x=0;x<a.NumberCratesToMove;x++)
                {
                    if(MoveMultiple)
                    {
                        tempStack.Push(input[a.MoveFrom-1].Pop());
                    }
                    else
                    {
                        input[a.MoveTo-1].Push(input[a.MoveFrom-1].Pop());
                    }
                }
                if(MoveMultiple)
                {
                    while(tempStack.Count()>0)
                    {
                        input[a.MoveTo-1].Push(tempStack.Pop());
                    }
                }
            }
        }

        private static void PrintStacks(Stack<string>[] input)
        {
            int i=0;
            foreach(var stack in input)
            {
                Console.WriteLine("Content in Stack {0}", i+1);
                foreach(var item in stack)
                {
                    Console.WriteLine(item);
                }
                i++;
            }
        }

        private static Stack<string>[] GetInitialState()
        {
            string input = @"    [H]         [H]         [V]    
    [V]         [V] [J]     [F] [F]
    [S] [L]     [M] [B]     [L] [J]
    [C] [N] [B] [W] [D]     [D] [M]
[G] [L] [M] [S] [S] [C]     [T] [V]
[P] [B] [B] [P] [Q] [S] [L] [H] [B]
[N] [J] [D] [V] [C] [Q] [Q] [M] [P]
[R] [T] [T] [R] [G] [W] [F] [W] [L]";

            var numStacks = (input.Split("\n").Max(x=>x.Length)+1)/4;

            Stack<string>[] stacks = new Stack<string>[numStacks];

            foreach(var row in input.Split("\n"))
            {
                var temp = row
                            //.Replace("   "," ")
                            //.Replace("      ","  ")
                            //.Replace("         ", "   ")
                            //.Replace("[","")
                            //.Replace("]","")
                            //.Replace("         ", "   ")
                            //.Replace("      ","  ")
                            ;
                Console.WriteLine(temp);
                
                for(int i=0;i<temp.Length;i=i+4)
                {
                    var stackPos = (i)/4;
                    if(temp[i+1]!=' ')
                    {
                        if(stacks[stackPos]==null) stacks[stackPos] = new Stack<string>();
                        stacks[stackPos].Push(temp[i+1].ToString());
                    }
                }
            }

            for(var i=0;i<stacks.Count();i++)
            {
                var tempStack = new Stack<string>();
                //Console.WriteLine(i);
                while(stacks[i].Count()>0)
                {
                    tempStack.Push(stacks[i].Pop());
                }
                stacks[i] = tempStack;
            }

            return stacks;
        }

